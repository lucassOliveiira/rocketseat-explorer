// Operadores lógicos (logical operators)

// 2 valores booleanos, quando verificados, resultará em true ou false

let pao = true;
let queijo = true;

// AND &&
console.log( pao && queijo);

// OR ||
console.log(pao || queijo)

// NOT ! 
console.log(!pao) // trocando/negando o valor dela, se for true vira false, se for false vira true