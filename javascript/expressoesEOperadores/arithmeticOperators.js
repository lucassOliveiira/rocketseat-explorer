// Operadores Aritiméticos

// Multiplicação
console.log(`Multiplicação: ${3.2 * 2}`)

// Divisão
console.log(`Divisão: ${1500 / 3}`)

// Soma
console.log(`Soma: ${849 + 500}`)

// Subtração
console.log(`Subtração: ${1289 - 982}`)

// Resto da divisão
console.log(`Resto da divisão: ${10 % 10}`)

// Incremento
let increment = 0
increment++
console.log(`Incremento: ${++increment}`)

// Decremento
let decrement = 3
decrement--
console.log(`Decremento: ${--decrement}`)

// Exponencial
console.log(`Exponencial: ${3 ** 3}`)

// Grouping operator
let total = (2 + 3) * 5
console.log(`Agrupamento: ${total}`)